// @desc    Forgot password
// @route   POST /api/auth/forgot-password
// @access  Public
router.post('/forgot-password', [
  body('email')
    .isEmail()
    .normalizeEmail()
    .withMessage('Please provide a valid email')
], async (req, res, next) => {
  try {
    // Check for validation errors
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
      return res.status(400).json({
        success: false,
        message: 'Validation error',
        errors: errors.array()
      });
    }

    const { email } = req.body;

    const user = await User.findOne({ email });

    if (!user) {
      return res.status(404).json({
        success: false,
        message: 'No user found with that email address'
      });
    }

    // Generate reset token
    const resetToken = crypto.randomBytes(20).toString('hex');

    // Hash token and set to resetPasswordToken field
    user.resetPasswordToken = crypto
      .createHash('sha256')
      .update(resetToken)
      .digest('hex');

    // Set expire time (10 minutes)
    user.resetPasswordExpire = Date.now() + 10 * 60 * 1000;

    await user.save({ validateBeforeSave: false });

    // Create reset URL
    const resetUrl = `${process.env.CLIENT_URL || 'http://localhost:3000'}/reset-password/${resetToken}`;

    try {
      // Send password reset email
      await sendEmail({
        email: user.email,
        subject: 'Password Reset Request - PrinTeeQ',
        html: getPasswordResetTemplate(resetUrl, user.email)
      });

      res.status(200).json({
        success: true,
        message: 'Password reset email sent successfully',
        data: `If an account with email ${email} exists, you will receive a password reset email shortly.`
      });
    } catch (emailError) {
      console.error('Email sending failed:', emailError);
      
      // Clear the reset token if email fails
      user.resetPasswordToken = undefined;
      user.resetPasswordExpire = undefined;
      await user.save({ validateBeforeSave: false });
      
      // Determine specific error message based on error type
      let errorMessage = 'Email could not be sent. Please try again later.';
      
      if (emailError.message.includes('Email configuration is incomplete')) {
        errorMessage = 'Email service is not configured. Please contact the administrator.';
      } else if (emailError.message.includes('Please update EMAIL_USER and EMAIL_PASS')) {
        errorMessage = 'Email service is not properly configured. Please contact the administrator.';
      } else if (emailError.code === 'EAUTH') {
        errorMessage = 'Email authentication failed. Please contact the administrator.';
      } else if (emailError.code === 'ECONNECTION' || emailError.code === 'ETIMEDOUT') {
        errorMessage = 'Unable to connect to email server. Please try again later.';
      }
      
      // For development, still return the URL if email fails
      if (process.env.NODE_ENV === 'development') {
        return res.status(200).json({
          success: true,
          message: 'Email sending failed, but here is the reset link for development',
          resetUrl,
          data: `Development mode: ${errorMessage} But you can use this link: ${resetUrl}`,
          error: emailError.message
        });
      }
      
      return res.status(500).json({
        success: false,
        message: errorMessage
      });
    }

  } catch (error) {
    next(error);
  }
});

// @desc    Reset password
// @route   PUT /api/auth/reset-password/:resettoken
// @access  Public
router.put('/reset-password/:resettoken', [
  body('password')
    .isLength({ min: 6 })
    .withMessage('Password must be at least 6 characters long')
], async (req, res, next) => {
  try {
    // Check for validation errors
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
      return res.status(400).json({
        success: false,
        message: 'Validation error',
        errors: errors.array()
      });
    }

    // Get hashed token
    const resetPasswordToken = crypto
      .createHash('sha256')
      .update(req.params.resettoken)
      .digest('hex');

    const user = await User.findOne({
      resetPasswordToken,
      resetPasswordExpire: { $gt: Date.now() }
    });

    if (!user) {
      return res.status(400).json({
        success: false,
        message: 'Invalid or expired password reset token'
      });
    }

    // Set new password
    user.password = req.body.password;
    user.resetPasswordToken = undefined;
    user.resetPasswordExpire = undefined;
    await user.save();

    sendTokenResponse(user, 200, res);
  } catch (error) {
    next(error);
  }
});

// @desc    Create first admin user (only if no admin exists)
// @route   POST /api/auth/create-first-admin
// @access  Public (but only works if no admin exists)
router.post('/create-first-admin', async (req, res, next) => {
  try {
    // Check if any admin user already exists
    const existingAdmin = await User.findOne({ role: 'admin' });
    if (existingAdmin) {
      return res.status(400).json({
        success: false,
        message: 'Admin user already exists. This endpoint is only for initial setup.'
      });
    }

    const { name, email, password } = req.body;

    // Validate input
    if (!name || !email || !password) {
      return res.status(400).json({
        success: false,
        message: 'Name, email, and password are required'
      });
    }

    // Create first admin user
    const admin = await User.create({
      name,
      email,
      password,
      role: 'admin',
      isVerified: true
    });

    sendTokenResponse(admin, 201, res);
  } catch (error) {
    next(error);
  }
});

